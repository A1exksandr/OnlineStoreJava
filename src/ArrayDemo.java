public class ArrayDemo {

    public static void main(String[] args) {
        /*
         * 1. ОДНОМЕРНЫЕ МАССИВЫ
         *
         * Общий синтаксис объявления:
         * Тип[] имяМассива;
         * имяМассива = new Тип[размер];
         *
         * Либо сразу:
         * Тип[] имяМассива = new Тип[размер];
         *
         * Также можно использовать "короткую" инициализацию:
         * Тип[] имяМассива = {значение1, значение2, ...};
         *
         * Одномерные массивы — это список элементов одного типа. Самая частая ошибка — выход за границы массива.
         */

        // Пример: массив названий товаров
        String[] products = {"Ноутбук", "Мышка", "Наушники", "Часы"};

        // Пример: массив цен на товары
        double[] prices = {100.0, 200.0, 300.0, 400.0};

        // Вывод списка товаров с использованием цикла for
        System.out.println("Список товаров и их цены (одномерные массивы):");
        for (int i = 0; i < products.length; i++) {
            System.out.println(products[i] + " - $" + prices[i]);
        }

        /*
         * 2. ДВУМЕРНЫЕ МАССИВЫ
         *
         * Общий синтаксис объявления:
         * Тип[][] имяМатрицы = new Тип[кол-воСтрок][кол-воСтолбцов];
         *
         * Либо:
         * Тип[][] имяМатрицы = {
         *   {elem00, elem01},
         *   {elem10, elem11}
         * };
         *
         * Двумерные массивы можно представлять себе как “таблицу” или “матрицу”.
         * Важно понять, что в каждой строке может быть разное количество столбцов (т.е. orders[0].length может отличаться от orders[1].length).
         */

        // Пример: матрица заказов (3 строки - 3 заказа, 4 столбца - по количеству товаров)
        // orders[i][j] показывает, сколько единиц j-го товара содержится в i-м заказе
        int[][] orders = {
                {1, 2, 0, 1}, // заказ №1
                {0, 1, 3, 0}, // заказ №2
                {2, 0, 1, 1}  // заказ №3
        };

        System.out.println("\nИнформация по заказам (двумерные массивы):");
        for (int i = 0; i < orders.length; i++) {
            System.out.println("Заказ №" + (i + 1) + ": ");
            for (int j = 0; j < orders[i].length; j++) {
                int quantity = orders[i][j];
                String productName = products[j];
                double cost = quantity * prices[j];
                System.out.println(productName + " x " + quantity + " = $" + cost);
            }
            System.out.println();
        }

        /*
         * ДОПОЛНИТЕЛЬНЫЙ ПРИМЕР РАБОТЫ С МАССИВАМИ
         * Попробуем найти сумму всех товаров в каждом заказе:
         */
        System.out.println("Суммарная стоимость товаров в каждом заказе:");
        for (int i = 0; i < orders.length; i++) {
            double totalCost = 0;
            for (int j = 0; j < orders[i].length; j++) {
                totalCost += orders[i][j] * prices[j];
            }
            System.out.println("Заказ №" + (i + 1) + ": $" + totalCost);
        }

        /*
         * Типичные ошибки при работе с массивами:
         * - Выход за границы массива (ArrayIndexOutOfBoundsException),
         *   например, обратиться к products[5], когда длина массива 4.
         * - Неверная инициализация размерности двумерных массивов,
         *   например, не совпадает количество столбцов в каждой строке.
         * - Путаница в типах данных: когда пытаются в double[] записать String и т.п.
         */

        // --- НИЖЕ ПРИВЕДЕНЫ НЕСКОЛЬКО ДОМАШНИХ ЗАДАНИЙ ---

        /*
         * ДЗ 1 (простое):
         * 1) Создайте массив из 5 целых чисел (int[]), заполните его произвольными числами.
         * 2) Выведите все элементы массива в консоль.
         * 3) Найдите сумму всех элементов массива и также выведите в консоль.
         */

        /*
         * ДЗ 2 (посложнее):
         * 1) Создайте двумерный массив (например, 3x3) и заполните его любыми целыми числами.
         * 2) Посчитайте сумму каждой строки и каждого столбца, выведите результаты.
         * 3) (опционально) Найдите максимальный элемент в двумерном массиве и его индексы (i, j).
         */

        /*
         * ДЗ 3 (для закрепления и размышления):
         * 1) Напишите метод, который на вход принимает одномерный массив int[] и возвращает среднее значение элементов.
         *    (Среднее = сумма элементов / количество элементов).
         * 2) В методе main протестируйте этот метод на разных наборах данных.
         */
    }
}
